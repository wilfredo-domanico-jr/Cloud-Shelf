<?php

use Livewire\Volt\Component;
use App\Models\Folder;
use Illuminate\Support\Facades\Auth;

new class extends Component
{
    public $name = '';
    public $parent_id = null;

    public function createFolder()
    {
        $this->validate([
            'name' => 'required|string|max:255',
        ]);

        // prevent duplicate in same folder
        $exists = Folder::where('user_id', Auth::id())
            ->where('parent_id', $this->parent_id)
            ->where('name', $this->name)
            ->exists();

        if ($exists) {
            $this->addError('name', 'Folder already exists');
            return;
        }

        Folder::create([
            'user_id' => Auth::id(),
            'parent_id' => $this->parent_id,
            'name' => $this->name,
        ]);

        // reset input
        $this->reset('name');

        // close modal
        $this->dispatch('close-modal', name: 'create-folder');

        // optional refresh event
        $this->dispatch('folder-created');
    }
};
